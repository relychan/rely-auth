// ClientIPLocation represents the location where the client IP should be parsed from.
type ClientIPLocation uint8

const (
	// ClientIPFromXForwardedFor represents the location of client IP read from the X-Forwarded-For header,
	// walking the chain right-to-left and skipping any IP that falls within one of the given trusted CIDR prefixes.
	// The first IP that is not trusted is the client.
	// ClientIPFromXForwardForTrustedProxies stores the client IP read from the X-Forwarded-For header, given the exact number of trusted reverse proxies between this server and the public internet.
	// It returns the IP at position len(xff) - numTrustedProxies in the merged X-Forwarded-For list — the IP added by the outermost of your trusted proxies,
	// the only IP in the chain that none of your proxies have allowed an attacker to forge.
	ClientIPFromXForwardedFor ClientIPLocation = iota
	// ClientIPFromHeader stores the client IP from a single-IP header set by your reverse proxy.
	// Only safe with headers your proxy unconditionally OVERWRITES on every request, e.g.:  X-Real-IP, X-Client-IP, CF-Connecting-IP.
	// If the header reaches us with multiple values (misconfigured proxy that appends, or a downstream proxy not stripping a client-supplied value),
	// the LAST value wins — that's the one set by the hop closest to us, and therefore the most trusted.
	// Fail-closed if the last value doesn't parse: no client IP is set rather than falling back to earlier (less-trusted) values.
	// v4-mapped IPv6 (::ffff:a.b.c.d) folds to plain v4 and IPv6 zones are stripped before storage.
	ClientIPFromHeader
	// ClientIPFromRemoteAddr stores the client IP read from the TCP RemoteAddr of the incoming request — the IP address of whoever opened the connection to this server.
	// Use this strategy when this server is directly connected to the public internet with NO reverse proxy in front of it.
	// Behind a reverse proxy, RemoteAddr is the proxy's IP, not the client's — use ClientIPFromHeader or ClientIPFromXFF instead.
	// IPv4 clients on a dual-stack listener surface as ::ffff:a.b.c.d; they fold to plain v4 before storage so one logical client maps to one key.
	// IPv6 zones are preserved (link-local connections may legitimately have one).
	ClientIPFromRemoteAddr
)

var enumValueClientIPLocation = []string{
	string(middlewares.ClientIPFromXForwardedFor),
	string(middlewares.ClientIPFromHeader),
	string(middlewares.ClientIPFromRemoteAddr),
}

// IsValid if the current instance is valid.
func (j ClientIPLocation) IsValid() bool {
	return j < 3
}

// String implements fmt.Stringer interface.
func (j ClientIPLocation) String() string {
	if !j.IsValid() {
		return ""
	}

	return enumValueClientIPLocation[j]
}

// MarshalText encodes the receiver into UTF-8-encoded text and returns the result.
func (j ClientIPLocation) MarshalText() ([]byte, error) {
	return []byte(j.String()), nil
}

// UnmarshalText must be able to decode the form generated by MarshalText.
func (j *ClientIPLocation) UnmarshalText(text []byte) error {
	result, err := ParseClientIPLocation(string(text))
	if err != nil {
		return err
	}

	*j = result

	return nil
}

// MarshalJSON marshals themselves into valid JSON.
func (j ClientIPLocation) MarshalJSON() ([]byte, error) {
	return goutils.QuoteBytes(j.String()), nil
}

// UnmarshalJSON unmarshals a JSON description of themselves.
func (j *ClientIPLocation) UnmarshalJSON(input []byte) error {
	value := bytes.Trim(input, `"`)

	return j.UnmarshalText(value)
}

// JSONSchema defines a custom definition for JSON schema.
func (ClientIPLocation) JSONSchema() *jsonschema.Schema {
	return &jsonschema.Schema{
		Type:        "string",
		Description: "The position of the IP to select. Default is the X-Forwarded-For header.",
		Enum:        goutils.ToAnySlice(GetClientIPLocations()),
		Default:     ClientIPFromXForwardedFor.String(),
	}
}

// ParseClientIPLocation parses a [ClientIPLocation] from string.
func ParseClientIPLocation(value string) (ClientIPLocation, error) {
	switch middlewares.ClientIPResolutionType(value) {
	case middlewares.ClientIPFromRemoteAddr:
		return ClientIPFromRemoteAddr, nil
	case middlewares.ClientIPFromHeader:
		return ClientIPFromHeader, nil
	case middlewares.ClientIPFromXForwardedFor:
		return ClientIPFromXForwardedFor, nil
	default:
		return 255, fmt.Errorf(
			"%w, got: %s",
			ErrInvalidClientIPLocation,
			value,
		)
	}
}

// GetClientIPLocations get the list of forwarded IP position enum.
func GetClientIPLocations() []string {
	return enumValueClientIPLocation
}

// RelyAuthIPAllowList holds security rules of the IP allow list from the parsed config.
type RelyAuthIPAllowList struct {
	goutils.ValidateIPOptions

	Headers                []string
	Location               ClientIPLocation
	TrustedProxyIPPrefixes []*net.IPNet
	NumTrustedProxies      int32
}

// AllowedIPsFromConfig creates a [RelyAuthIPAllowList] instance from config.
func AllowedIPsFromConfig(
	conf *RelyAuthIPAllowListConfig,
	getEnvFunc goenvconf.GetEnvFunc,
) (*RelyAuthIPAllowList, error) {
	if conf == nil || conf.RelyAuthAllowListConfig.IsZero() {
		return nil, ErrEmptyAllowedIPs
	}

	if getEnvFunc == nil {
		getEnvFunc = goenvconf.GetOSEnv
	}

	include, err := parseEnvSubnets(conf.Include, getEnvFunc)
	if err != nil {
		return nil, fmt.Errorf("failed to parse allowed IP: %w", err)
	}

	exclude, err := parseEnvSubnets(conf.Exclude, getEnvFunc)
	if err != nil {
		return nil, fmt.Errorf("failed to parse disallowed IP: %w", err)
	}

	location := conf.Location
	if !conf.Location.IsValid() {
		return nil, ErrInvalidClientIPLocation
	}

	result := &RelyAuthIPAllowList{
		ValidateIPOptions: goutils.ValidateIPOptions{
			AllowedIPRanges: include,
			BlockedIPRanges: exclude,
			PublicIPOnly:    conf.PublicOnly,
		},
		Location:          location,
		NumTrustedProxies: conf.NumTrustedProxies,
	}

	switch location {
	case ClientIPFromHeader:
		headers := make([]string, 0, len(conf.Headers))

		for i := range conf.Headers {
			header := strings.TrimSpace(conf.Headers[i])
			if header == "" {
				continue
			}

			headers = append(headers, strings.ToLower(header))
		}

		if len(headers) == 0 {
			return nil, ErrClientIPHeaderRequired
		}

		slices.Sort(headers)

		result.Headers = slices.Compact(headers)
	case ClientIPFromXForwardedFor:
		if len(conf.TrustedProxyIPPrefixes) > 0 {
			result.TrustedProxyIPPrefixes = make([]*net.IPNet, 0, len(conf.TrustedProxyIPPrefixes))

			for _, prefix := range conf.TrustedProxyIPPrefixes {
				prefix = strings.TrimSpace(prefix)
				if prefix == "" {
					continue
				}

				subnet, err := goutils.ParseSubnet(prefix)
				if err != nil {
					return nil, err
				}

				result.TrustedProxyIPPrefixes = append(result.TrustedProxyIPPrefixes, subnet)
			}
		}
	default:
	}

	return result, nil
}

// Validate checks if the request satisfies the security rule.
func (rai *RelyAuthIPAllowList) Validate(body *AuthenticateRequestData) error {
	clientIP, err := rai.GetClientIPs(body)
	if err != nil {
		return err
	}

	return goutils.ValidateIP(clientIP, rai.ValidateIPOptions)
}

// GetClientIPs gets the client IPs from request data.
func (rai *RelyAuthIPAllowList) GetClientIPs(data *AuthenticateRequestData) (net.IP, error) {
	if data == nil {
		return nil, ErrIPNotFound
	}

	switch rai.Location {
	case ClientIPFromHeader:
		if len(data.Headers) == 0 {
			return nil, ErrIPNotFound
		}

		for _, name := range rai.Headers {
			ip := parseHeaderAddr(data.Headers[name])
			if ip != nil {
				return ip, nil
			}
		}

		return nil, ErrInvalidIP
	case ClientIPFromXForwardedFor:
		if len(data.Headers) == 0 {
			return nil, ErrIPNotFound
		}

		return rai.parseXFFAddr(data.Headers["x-forwarded-for"])
	default:
		remoteAddr := data.RemoteAddr
		if remoteAddr == "" {
			return nil, ErrIPNotFound
		}

		host, _, err := net.SplitHostPort(remoteAddr)
		if err != nil {
			host = remoteAddr // RemoteAddr may already be a bare IP (e.g. in tests).
		}

		ip := parseHeaderAddr(host)
		if ip == nil {
			return nil, ErrInvalidIP
		}

		return ip, nil
	}
}

func (rai *RelyAuthIPAllowList) parseXFFAddr(value string) (net.IP, error) {
	if rai.NumTrustedProxies < 1 && len(rai.TrustedProxyIPPrefixes) == 0 {
		return parseHeaderAddr(value), nil
	}

	numTrustedProxies := rai.NumTrustedProxies
	if numTrustedProxies < 1 {
		numTrustedProxies = math.MaxInt32
	}

	var lastIP net.IP

	value = strings.TrimSpace(value)
	// walks the entries of the merged X-Forwarded-For chain RIGHT-TO-LEFT, invoking visit on each trimmed non-empty entry. visit returns true to stop the walk.
	// Lazy walk, zero allocations (entries are substrings of the input headers).
	// Multiple XFF headers are merged per RFC 2616 — each header's comma-separated entries in order received — so an attacker cannot pick which value security logic sees by sending a duplicate header.
	for value != "" {
		var v string

		i := strings.LastIndexByte(value, ',')
		if i >= 0 {
			v, value = value[i+1:], value[:i]
		} else {
			v, value = value, ""
		}

		v = strings.TrimSpace(v)
		if v == "" {
			if value == "" {
				return lastIP, nil
			}

			continue
		}

		ip := parseHeaderAddr(v)
		if ip == nil {
			return nil, ErrInvalidIP
		}

		lastIP = ip
		numTrustedProxies--

		if (len(rai.TrustedProxyIPPrefixes) > 0 &&
			!inAnyIPPrefix(ip, rai.TrustedProxyIPPrefixes)) || numTrustedProxies == 0 {
			return ip, nil
		}
	}

	return nil, ErrIPNotFound
}

func parseHeaderAddr(value string) net.IP {
	value = strings.TrimSpace(value)
	if value == "" {
		return nil
	}

	commaIndex := strings.LastIndexByte(value, ',')
	if commaIndex == -1 {
		return net.ParseIP(value)
	}

	// Multiple values in a single-IP header: the LAST entry (rightmost) is set by the
	// hop closest to this server and is therefore the most trusted.
	part := strings.TrimSpace(value[commaIndex+1:])
	if part == "" {
		return nil
	}

	return net.ParseIP(part)
}

// inAnyIPPrefix reports whether ip falls within any of the given prefixes.
func inAnyIPPrefix(ip net.IP, prefixes []*net.IPNet) bool {
	for _, p := range prefixes {
		if p.Contains(ip) {
			return true
		}
	}

	return false
}
